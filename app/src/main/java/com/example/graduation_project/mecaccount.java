    public void create(String mecstore){

        String url = "http://10.0.2.2:84/graduation_project/create_mec_account.php";
        RequestQueue queue = Volley.newRequestQueue(mecaccount.this);
        StringRequest request = new StringRequest(Request.Method.POST, url, new com.android.volley.Response.Listener<String>() {
            @Override
            public void onResponse(String response) {
                Log.e("TAG", "RESPONSE IS " + response);
                try {
                    JSONObject jsonObject = new JSONObject(response);
                    // on below line we are displaying a success toast message.
                    Toast.makeText(mecaccount.this,
                            jsonObject.getString("message"), Toast.LENGTH_SHORT).show();
                } catch (JSONException e) {
                    e.printStackTrace();
                }

            }
        }, new com.android.volley.Response.ErrorListener() {
            @Override
            public void onErrorResponse(VolleyError error) {
                // method to handle errors.
                Toast.makeText(mecaccount.this,
                        "Fail to get response = " + error, Toast.LENGTH_SHORT).show();
            }
        }) {
            @Override
            public String getBodyContentType() {

                return "application/x-www-form-urlencoded; charset=UTF-8";
            }

            @Override
            protected Map<String, String> getParams() {


                Map<String, String> params = new HashMap<String, String>();


                params.put("username", username1);
                params.put("mecname", mecstore);
                if(lat!=null)
                params.put("latitude", lat);
                else
                    params.put("latitude","17.55");
                if(log!=null)
                params.put("longitude", log);
else
                    params.put("longitude", "18.55");


                return params;
            }
        };

        queue.add(request);






    }
